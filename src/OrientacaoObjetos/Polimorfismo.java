package OrientacaoObjetos;

public class Polimorfismo {
    public static void main(String[] args) {
        /*
        ------------- POLIMORFISMO -------------
        - O termo "polimorfismo" significa "muitas formas" e refere-se à capacidade de responderem a mesma
        mensagem de diferentes maneiras.
        - O polimorfismo é alcançado por meio de dois mecanismos principais:
            - sobrescrita de métodos (overriding);
            - sobrecarga de métodos (overloading).
          SOBRESCRITA DE MÉTODOS (OVERRIDING)
          - É a capacidade de uma classe filha fornecer uma implementação diferente de um metodo que já
          está definido na classe pai;
          - Substitui o comportamento padrão de um metodo da classe pai com uma implementação personalizada;
          - A classe filha deve ter um metodo com a mesma assinatura (nome, tipo e ordem dos parâmetros) do
           metodo que deseja substituir na classe pai;
           SOBRECARGA DE MÉTODOS (OVERLOADING)
           - É a capacidade de uma classe ter vários métodos com o mesmo nome, mas com diferentes parâmetros;
           - Para ocorrer a sobrecarga de métodos, os métodos devem ter o mesmo nome, mas devem ter
           assinaturas diferentes (número, tipo ou ordem dos parâmetros);
           - Devem ter tipo, ordem ou quantidade de parâmetros diferentes dos originais;
           - Sobrecarga é utilizada nos construtores de classes (Próximo assunto);
        * */
    }
}
