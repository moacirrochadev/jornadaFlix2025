public class MetodosConstrutores {
    public static void main(String[] args) {
        /*
        CONSTRUTORES DE CLASSES
        - É um metodo especial em uma classe Java, usado para criar objetos dessa classe;
        - O construtor tem o mesmo nome da classe e não possui tipo de retorno declarado;
        - É chamado automaticamente quando um novo objeto da classe é criado usando o operador 'new';
        Ex.:
        public class Pessoa {
            String nome;
            int idade;
            // Construtor
            public Pessoa(String nome, int idade) {
                this.nome = nome;
                this.idade = idade;
            }
        }
        * */
    }
}
